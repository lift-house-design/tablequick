$(function(){
	$('input[name="email_enabled"]').change(function(){
		if($(this).filter(':checked').length)
		{
			$(this)
				.parents('.field')
				.next('.field')
				.removeClass('hidden')
				.next('.field')
				.removeClass('hidden');
		}
		else
		{
			$(this)
				.parents('.field')
				.next('.field')
				.addClass('hidden')
				.next('.field')
				.addClass('hidden');
		}
	});
	$('input[name="sms_enabled"]').change(function(){
		if($(this).filter(':checked').length)
		{
			$(this)
				.parents('.field')
				.next('.field')
				.removeClass('hidden');
		}
		else
		{
			$(this)
				.parents('.field')
				.next('.field')
				.addClass('hidden');
		}
	});
});